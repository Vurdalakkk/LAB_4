// Подгрузка необходимых библиотек

#include <stdio.h>
#include <locale.h>

// Объявление функции

int main() {
	// Локализация для русского языка

	setlocale(LC_CTYPE, "RUS");

	// Объявление переменных

	char c;
	int i;
	float f;
	double d;

	// Ввод значений из консоли

	printf("Введите символ: ");
	scanf("%c", &c);

	printf("Введите целое число: ");
	scanf("%i", &i);

	printf("Введите число с плавающей точкой: ");
	scanf("%f", &f);

	printf("Введите число вида  \"5e-12\": ");
	scanf("%lf", &d);
	// Деление вещественного числа на целую и дробную часть

	int ceil = (int)f;
	float fraction = f - ceil;



	// Вывод значений

	printf("Ваши значения: \n Символ - %c \n Целое число - %i \n Число с плавающей точкой - %.4f \n Число в научной нотации - %e \n Целая часть вещ. числа: %d \n ", c, i, f, d, ceil);
	printf("Дробная часть вещ. числа: %.4f \n Десятичный код символа: %d \n Шестнадцатиричный код символа: %x \n 1/i: %f", fraction, c, c, 1.0 / i);
}
